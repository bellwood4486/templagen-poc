package gen

import (
	"fmt"
	"go/format"
	"maps"
	"path/filepath"
	"slices"
	"strings"

	"github.com/bellwood4486/tmpltype/internal/scan"
	"github.com/bellwood4486/tmpltype/internal/typing"
	"github.com/bellwood4486/tmpltype/internal/util"
)

// Unit は単一のテンプレート処理単位
type Unit struct {
	Pkg           string // 出力パッケージ名
	SourcePath    string // 埋め込むテンプレファイルのパス（go:embedディレクティブで使用）
	SourceLiteral string // テンプレ本文
}

// templateData は各テンプレートごとの処理結果を保持
type templateData struct {
	name       string              // テンプレート名（例: "user", "mail_invite/title"）
	groupName  string              // グループ名（例: "mail_invite"）。フラットなら空文字列
	typeName   string              // 型名（例: "User", "MailInviteTitle"）
	sourcePath string              // 埋め込むファイルパス
	varName    string              // embed変数名（例: "userTplSource"）
	typed      *typing.TypedSchema // 型情報
}

// groupInfo はグループごとの情報を保持
type groupInfo struct {
	name      string         // グループ名（例: "mail_invite"）
	typeName  string         // グループの型名（例: "MailInvite"）
	templates []templateData // グループ内のテンプレート
}

// preparedData はコード生成に必要なすべての準備済みデータを保持
type preparedData struct {
	pkg       string
	imports   map[string]struct{}
	templates []templateData // 全テンプレート（フラット + グループ）
	groups    []groupInfo    // グループ情報（フラットなテンプレートは含まない）
	flatTemplates []templateData // フラットなテンプレートのみ
}

// prepareTemplateData はテンプレートをスキャンし、型を解決して、コード生成に必要なデータを準備する
func prepareTemplateData(units []Unit) (*preparedData, error) {
	if len(units) == 0 {
		return nil, fmt.Errorf("no units provided")
	}

	templates := make([]templateData, 0, len(units))
	allImports := make(map[string]struct{})

	// デフォルトのimport
	allImports["io"] = struct{}{}
	allImports["text/template"] = struct{}{}
	allImports["embed"] = struct{}{}
	allImports["fmt"] = struct{}{}

	// 各テンプレートを処理
	for _, unit := range units {
		// テンプレート名を抽出 (例: "mail_invite/title" または "footer")
		templateName := extractTemplateName(unit.SourcePath)

		// グループ名を抽出 (スラッシュが含まれていればグループ)
		var groupName string
		var localName string
		if strings.Contains(templateName, "/") {
			parts := strings.Split(templateName, "/")
			groupName = parts[0]
			localName = parts[1]
		} else {
			localName = templateName
		}

		// 型名を生成 (例: "MailInviteTitle" または "Footer")
		var typeName string
		if groupName != "" {
			typeName = util.Export(groupName) + util.Export(localName)
		} else {
			typeName = util.Export(localName)
		}

		// embed変数名を生成 (スラッシュをアンダースコアに変換)
		varName := strings.ReplaceAll(templateName, "/", "_") + "TplSource"

		// テンプレートをスキャン
		sch, err := scan.ScanTemplate(unit.SourceLiteral)
		if err != nil {
			return nil, fmt.Errorf("failed to scan template %s: %w", unit.SourcePath, err)
		}

		// 型解決
		typed, err := typing.Resolve(sch, unit.SourceLiteral)
		if err != nil {
			return nil, fmt.Errorf("failed to resolve types for %s: %w", unit.SourcePath, err)
		}

		// テンプレートデータを追加
		templates = append(templates, templateData{
			name:       templateName,
			groupName:  groupName,
			typeName:   typeName,
			sourcePath: unit.SourcePath,
			varName:    varName,
			typed:      typed,
		})
	}

	// テンプレート名でソート（出力を安定させるため）
	slices.SortFunc(templates, func(a, b templateData) int {
		return strings.Compare(a.name, b.name)
	})

	// グループ情報を整理
	groups, flatTemplates := organizeGroups(templates)

	return &preparedData{
		pkg:           units[0].Pkg, // すべて同じパッケージ名のはず
		imports:       allImports,
		templates:     templates,
		groups:        groups,
		flatTemplates: flatTemplates,
	}, nil
}

// organizeGroups はテンプレートをグループとフラットに分類する
func organizeGroups(templates []templateData) ([]groupInfo, []templateData) {
	groupMap := make(map[string][]templateData)
	var flatTemplates []templateData

	for _, tmpl := range templates {
		if tmpl.groupName != "" {
			groupMap[tmpl.groupName] = append(groupMap[tmpl.groupName], tmpl)
		} else {
			flatTemplates = append(flatTemplates, tmpl)
		}
	}

	// グループ情報を構築
	var groups []groupInfo
	for groupName, groupTemplates := range groupMap {
		groups = append(groups, groupInfo{
			name:      groupName,
			typeName:  util.Export(groupName),
			templates: groupTemplates,
		})
	}

	// グループ名でソート
	slices.SortFunc(groups, func(a, b groupInfo) int {
		return strings.Compare(a.name, b.name)
	})

	return groups, flatTemplates
}

// Emit は複数のテンプレートから1つの統合Goファイルを生成する
// 単一テンプレートの場合も同じフォーマットで生成される
func Emit(units []Unit) (string, error) {
	// Phase 1: データ収集と準備
	prepared, err := prepareTemplateData(units)
	if err != nil {
		return "", err
	}

	// Phase 2: コード生成
	var b strings.Builder
	generateHeader(&b, prepared.pkg)
	generateImports(&b, prepared.imports)
	generateTemplateNamespace(&b, prepared)
	generateEmbedDeclarations(&b, prepared.templates)
	generateTemplateInitialization(&b, prepared)
	generateTemplatesFunction(&b)
	generateGenericRenderFunction(&b)
	generateTemplateBlocks(&b, prepared.templates)

	// Phase 3: フォーマット
	return formatCode(b.String())
}

// generateHeader はパッケージ宣言とコメントを生成する
func generateHeader(b *strings.Builder, pkg string) {
	write(b, "// Code generated by tmpltype; DO NOT EDIT.\n")
	write(b, "package %s\n\n", pkg)
}

// generateImports はimportセクションを生成する
func generateImports(b *strings.Builder, imports map[string]struct{}) {
	write(b, "import (\n")
	keys := slices.Sorted(maps.Keys(imports))
	for _, k := range keys {
		if k == "embed" {
			write(b, "\t_ %q\n", k)
		} else {
			write(b, "\t%q\n", k)
		}
	}
	write(b, ")\n\n")
}

// generateTemplateNamespace はTemplateName型と名前空間を生成する
func generateTemplateNamespace(b *strings.Builder, prepared *preparedData) {
	write(b, "// TemplateName is a type-safe template name\n")
	write(b, "type TemplateName string\n\n")
	write(b, "// Template provides type-safe access to template names\n")
	write(b, "var Template = struct {\n")

	// フラットなテンプレート
	for _, tmpl := range prepared.flatTemplates {
		write(b, "\t%s TemplateName\n", tmpl.typeName)
	}

	// グループ
	for _, group := range prepared.groups {
		write(b, "\t%s struct {\n", group.typeName)
		for _, tmpl := range group.templates {
			// ローカル名（グループプレフィックスなし）を取得
			localName := strings.TrimPrefix(tmpl.typeName, group.typeName)
			write(b, "\t\t%s TemplateName\n", localName)
		}
		write(b, "\t}\n")
	}

	write(b, "}{\n")

	// フラットなテンプレートの初期化
	for _, tmpl := range prepared.flatTemplates {
		write(b, "\t%s: %q,\n", tmpl.typeName, tmpl.name)
	}

	// グループの初期化
	for _, group := range prepared.groups {
		write(b, "\t%s: struct {\n", group.typeName)
		for _, tmpl := range group.templates {
			localName := strings.TrimPrefix(tmpl.typeName, group.typeName)
			write(b, "\t\t%s TemplateName\n", localName)
		}
		write(b, "\t}{\n")
		for _, tmpl := range group.templates {
			localName := strings.TrimPrefix(tmpl.typeName, group.typeName)
			write(b, "\t\t%s: %q,\n", localName, tmpl.name)
		}
		write(b, "\t},\n")
	}

	write(b, "}\n\n")
}

// generateEmbedDeclarations は各テンプレートのembed宣言を生成する
func generateEmbedDeclarations(b *strings.Builder, templates []templateData) {
	for _, tmpl := range templates {
		write(b, "//go:embed %s\n", tmpl.sourcePath)
		write(b, "var %s string\n\n", tmpl.varName)
	}
}

// generateTemplateInitialization はテンプレート初期化のためのヘルパー関数とマップを生成する
func generateTemplateInitialization(b *strings.Builder, prepared *preparedData) {
	// Helper function for template initialization
	write(b, "func newTemplate(name TemplateName, source string) *template.Template {\n")
	write(b, "\treturn template.Must(template.New(string(name)).Option(%q).Parse(source))\n", "missingkey=error")
	write(b, "}\n\n")

	// Templates map - initialized once at package initialization
	write(b, "var templates = map[TemplateName]*template.Template{\n")

	// フラットなテンプレート
	for _, tmpl := range prepared.flatTemplates {
		fieldRef := "Template." + tmpl.typeName
		write(b, "\t%s: newTemplate(%s, %s),\n",
			fieldRef, fieldRef, tmpl.varName)
	}

	// グループ内のテンプレート
	for _, group := range prepared.groups {
		for _, tmpl := range group.templates {
			localName := strings.TrimPrefix(tmpl.typeName, group.typeName)
			fieldRef := "Template." + group.typeName + "." + localName
			write(b, "\t%s: newTemplate(%s, %s),\n",
				fieldRef, fieldRef, tmpl.varName)
		}
	}

	write(b, "}\n\n")
}

// generateTemplatesFunction はTemplates()関数を生成する
func generateTemplatesFunction(b *strings.Builder) {
	write(b, "// Templates returns a map of all templates\n")
	write(b, "func Templates() map[TemplateName]*template.Template {\n")
	write(b, "\treturn templates\n")
	write(b, "}\n\n")
}

// generateGenericRenderFunction は汎用Render関数を生成する
func generateGenericRenderFunction(b *strings.Builder) {
	write(b, "// Render renders a template by name with the given data\n")
	write(b, "func Render(w io.Writer, name TemplateName, data any) error {\n")
	write(b, "\ttmpl, ok := templates[name]\n")
	write(b, "\tif !ok {\n")
	write(b, "\t\treturn fmt.Errorf(\"template %%q not found\", name)\n")
	write(b, "\t}\n")
	write(b, "\treturn tmpl.Execute(w, data)\n")
	write(b, "}\n\n")
}

// generateTemplateBlocks は各テンプレートごとの型定義とRender関数を生成する
func generateTemplateBlocks(b *strings.Builder, templates []templateData) {
	generatedTypes := make(map[string]bool)

	for _, tmpl := range templates {
		// テンプレートブロックのセパレータ
		write(b, "// ============================================================\n")
		write(b, "// %s template\n", tmpl.name)
		write(b, "// ============================================================\n\n")

		generateNamedTypes(b, tmpl, generatedTypes)
		generateParamType(b, tmpl)
		generateRenderFunction(b, tmpl)
	}
}

// generateNamedTypes は名前付き型を生成する
func generateNamedTypes(b *strings.Builder, tmpl templateData, generatedTypes map[string]bool) {
	for _, namedType := range tmpl.typed.NamedTypes {
		// 型名の衝突を避けるため、プレフィックスを付ける
		typeName := tmpl.typeName + namedType.Name
		if generatedTypes[typeName] {
			continue // すでに生成済み
		}
		generatedTypes[typeName] = true

		write(b, "type %s struct {\n", typeName)
		// フィールドをソートして順序を安定化
		fieldNames := slices.Sorted(maps.Keys(namedType.Fields))
		for _, fieldName := range fieldNames {
			field := namedType.Fields[fieldName]
			// フィールドの型名も調整が必要な場合がある
			goType := adjustTypeForTemplate(field.GoType, tmpl.typeName)
			write(b, "\t%s %s\n", field.Name, goType)
		}
		write(b, "}\n\n")
	}
}

// generateParamType はメインのパラメータ型を生成する
func generateParamType(b *strings.Builder, tmpl templateData) {
	write(b, "// %s represents parameters for %s template\n", tmpl.typeName, tmpl.name)
	write(b, "type %s struct {\n", tmpl.typeName)
	// トップレベルフィールドをソートして順序を安定化
	topFieldNames := slices.Sorted(maps.Keys(tmpl.typed.Fields))
	for _, fieldName := range topFieldNames {
		field := tmpl.typed.Fields[fieldName]
		// フィールドの型名も調整が必要な場合がある
		goType := adjustTypeForTemplate(field.GoType, tmpl.typeName)
		write(b, "\t%s %s\n", field.Name, goType)
	}
	write(b, "}\n\n")
}

// generateRenderFunction は型安全なRender関数を生成する
func generateRenderFunction(b *strings.Builder, tmpl templateData) {
	funcName := "Render" + tmpl.typeName

	// フィールド参照を構築 (グループ対応)
	var fieldRef string
	if tmpl.groupName != "" {
		groupTypeName := util.Export(tmpl.groupName)
		localName := strings.TrimPrefix(tmpl.typeName, groupTypeName)
		fieldRef = "Template." + groupTypeName + "." + localName
	} else {
		fieldRef = "Template." + tmpl.typeName
	}

	write(b, "// %s renders the %s template\n", funcName, tmpl.name)
	write(b, "func %s(w io.Writer, p %s) error {\n", funcName, tmpl.typeName)
	write(b, "\ttmpl, ok := templates[%s]\n", fieldRef)
	write(b, "\tif !ok {\n")
	write(b, "\t\treturn fmt.Errorf(\"template %%q not found\", %s)\n", fieldRef)
	write(b, "\t}\n")
	write(b, "\treturn tmpl.Execute(w, p)\n")
	write(b, "}\n\n")
}

// formatCode はgo/formatでコードをフォーマットする
func formatCode(code string) (string, error) {
	formatted, err := format.Source([]byte(code))
	if err != nil {
		return "", fmt.Errorf("failed to format generated code: %w", err)
	}
	return string(formatted), nil
}

// extractTemplateName はファイルパスからテンプレート名を抽出する
// グループ対応: ディレクトリを含むパスを返す
// 例: "templates/user_list.tmpl" -> "user_list" (フラット)
// 例: "templates/mail_invite/title.tmpl" -> "mail_invite/title" (グループ)
// 例: "email.tmpl" -> "email" (フラット)
func extractTemplateName(path string) string {
	// 拡張子を削除
	pathWithoutExt := strings.TrimSuffix(path, filepath.Ext(path))

	// ディレクトリ区切りで分割
	parts := strings.Split(filepath.ToSlash(pathWithoutExt), "/")

	// 最後の2つのパーツを取得（ディレクトリ/ファイル名 または ファイル名のみ）
	var relevantParts []string
	if len(parts) >= 2 {
		// templates/mail_invite/title.tmpl -> ["mail_invite", "title"]
		relevantParts = parts[len(parts)-2:]

		// 最後から2番目がtemplatesディレクトリの場合は、ファイル名のみ
		if relevantParts[0] == "templates" {
			relevantParts = parts[len(parts)-1:]
		}
	} else {
		// ファイル名のみ
		relevantParts = parts
	}

	// 各パーツから数字プレフィックスを削除してクリーンアップ
	for i, part := range relevantParts {
		relevantParts[i] = cleanName(part)
	}

	// パスとして結合
	return strings.Join(relevantParts, "/")
}

// cleanName は名前から数字プレフィックスを削除し、ハイフンをアンダースコアに変換する
func cleanName(name string) string {
	// 数字プレフィックスを削除（例: "01_header" -> "header"）
	if len(name) > 3 && name[0] >= '0' && name[0] <= '9' {
		if name[1] >= '0' && name[1] <= '9' {
			if name[2] == '_' || name[2] == '-' {
				name = name[3:]
			}
		} else if name[1] == '_' || name[1] == '-' {
			name = name[2:]
		}
	}

	// ハイフンをアンダースコアに変換
	name = strings.ReplaceAll(name, "-", "_")

	return name
}

// adjustTypeForTemplate は型名をテンプレート固有に調整する
func adjustTypeForTemplate(goType string, templatePrefix string) string {
	// 名前付き型への参照を調整
	// 例: "[]ItemsItem" -> "[]UserItemsItem" (Userテンプレートの場合)
	// これは簡略化された実装。実際にはより複雑な型の処理が必要

	// スライスの場合
	if strings.HasPrefix(goType, "[]") {
		elemType := goType[2:]
		if !isBuiltinType(elemType) && !strings.Contains(elemType, ".") {
			// カスタム型の場合、プレフィックスを付ける
			return "[]" + templatePrefix + elemType
		}
	}

	// マップの場合
	if strings.HasPrefix(goType, "map[string]") {
		elemType := goType[11:] // "map[string]" の後の部分
		if !isBuiltinType(elemType) && !strings.Contains(elemType, ".") {
			return "map[string]" + templatePrefix + elemType
		}
	}

	// 単純な名前付き型の場合
	if !isBuiltinType(goType) && !strings.Contains(goType, ".") &&
		!strings.Contains(goType, "[") && !strings.HasPrefix(goType, "*") {
		return templatePrefix + goType
	}

	return goType
}

func isBuiltinType(typeName string) bool {
	builtins := []string{
		"string", "int", "int8", "int16", "int32", "int64",
		"uint", "uint8", "uint16", "uint32", "uint64",
		"float32", "float64", "bool", "byte", "rune", "any",
		"time.Time", "error",
	}
	for _, b := range builtins {
		if typeName == b {
			return true
		}
	}
	return false
}

// write は strings.Builder への書き込みヘルパー
// 万が一失敗した場合は panic する
func write(b *strings.Builder, format string, args ...any) {
	_, err := fmt.Fprintf(b, format, args...)
	if err != nil {
		panic(err)
	}
}
